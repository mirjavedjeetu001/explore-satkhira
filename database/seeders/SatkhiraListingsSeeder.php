     * Real Satkhira district listings data.
     * Categories: 1-Home Tutor, 2-To-Let, 3-Restaurant, 4-Hospital, 5-School, 6-College,
     *             7-Pharmacy, 8-Doctor, 9-Government Office, 10-Bank, 11-Joruri Sheba,
     *             12-Samajik Songothon, 13-Tourist Spots, 14-Shopping Center,
     *             15-Online Services, 16-Residential Hotel, 17-Business Establishments,
     *             18-Digital Services, 20-Transport, 21-Job Circular, 22-Events, 23-Newspaper
     * Upazilas: 1-Satkhira Sadar, 2-Kaliganj, 3-Shyamnagar, 4-Assasuni, 5-Debhata, 6-Kalaroa, 7-Tala
     */
    public function run(): void
    {
        $userId = 4; // Mir Javed Jeetu
        $status = 'pending';

        $listings = $this->getListingsData();

        $inserted = 0;
        $skipped = 0;

        foreach ($listings as $listing) {
            // Check for duplicates by title
            $exists = Listing::where('title', $listing['title'])
                ->orWhere('title_bn', $listing['title_bn'])
                ->exists();

            if ($exists) {
                $skipped++;
                continue;
            }

            Listing::create(array_merge($listing, [
                'user_id' => $userId,
                'status' => $status,
                'slug' => Str::slug($listing['title']) . '-' . Str::random(5),
                'views' => 0,
                'is_featured' => false,
            ]));

            $inserted++;
        }

        $this->command->info("Inserted: {$inserted}, Skipped (duplicates): {$skipped}");
    }

